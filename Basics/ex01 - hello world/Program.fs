printfn "Hello world!"

// string:
printfn "%s" "hello"

// int:
printfn "%i" 42

// float:
printfn "%f" 3.14

// float with given precision
printfn "%.1f" 3.14

// tuples, records, biginteger and union types:
printfn "%A" (5, 6)

// Any object:
printfn "%O" 4

// bool:
printfn "%b" true

// exponential format
printfn "%e" 100.0
